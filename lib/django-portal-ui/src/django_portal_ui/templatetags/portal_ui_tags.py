@register.simple_tag
def icon(name, css_class='w-5 h-5'):
    """Render a Feather icon as inline SVG."""
    path = FEATHER_ICONS.get(name, FEATHER_ICONS.get('alert-circle'))
    # Include width/height attributes as fallback when CSS fails to load
    svg = f'''<svg class="{css_class}" width="24" height="24" fill="none" stroke="currentColor" viewBox="0 0 24 24" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">{path}</svg>'''
    return mark_safe(svg)


@register.simple_tag(takes_context=True)
def sidebar_item(context, label, url_name, icon_name, badge=None):
    """Render a sidebar navigation item."""
    request = context.get('request')

    try:
        url = reverse(url_name)
        is_active = request.path.startswith(url) if request else False
    except NoReverseMatch:
        url = '#'
        is_active = False

    active_class = 'bg-primary-100 text-primary-700' if is_active else 'text-gray-600 hover:bg-gray-100'
    icon_html = icon(icon_name, 'w-5 h-5')

    badge_html = ''
    if badge:
        badge_html = f'<span class="ml-auto bg-primary-500 text-white text-xs px-2 py-0.5 rounded-full">{badge}</span>'

    html = f'''
    <a href="{url}" class="flex items-center px-4 py-2 text-sm font-medium rounded-lg {active_class}">
        {icon_html}
        <span class="ml-3">{label}</span>
        {badge_html}
    </a>
    '''
    return mark_safe(html)


@register.inclusion_tag('portal_ui/components/messages.html', takes_context=True)
def show_messages(context):
    """Render flash messages."""
    return {'messages': context.get('messages', [])}


@register.filter
def add_class(field, css_class):
    """Add CSS class(es) to a form field widget.

    Usage:
        {{ form.name|add_class:"w-full px-3 py-2 border rounded-lg" }}

    If the widget already has classes, the new classes are appended.
    """
    existing_classes = field.field.widget.attrs.get('class', '')
    if existing_classes:
        new_classes = f"{existing_classes} {css_class}"
    else:
        new_classes = css_class

    return field.as_widget(attrs={'class': new_classes})
